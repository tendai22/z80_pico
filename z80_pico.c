#define WAIT_NOP(x) do { int count = x; while (count-- > 0) asm volatile("nop"); } while(0);

static inline void put_138(int num)
{
    gpio_put_masked(GPIO_138_MASK, num<<18);
}

static inline void put_out(int data)
{
    gpio_put_masked(GPIO_DO_MASK, data<<8);
}

static uint32_t co_value = 0;

void init_co(uint32_t data)
{
    co_value = data & CO_MASK;
}

static inline void set_co(uint32_t pin_mask)
{
    co_value |= pin_mask;
    gpio_put_masked(GPIO_DO_MASK, co_value);
    put_138(7);
    put_138(0);
}

static inline void clr_co(uint32_t pin_mask)
{
    co_value &= ~pin_mask;
    gpio_put_masked(GPIO_DO_MASK, co_value);
    put_138(7);
    put_138(0);
}

static inline void put_do(uint32_t data)
{
    gpio_put_masked(GPIO_DO_MASK, data<<8);
    put_138(5);
    //asm volatile ("nop");
    put_138(0);
}

static inline void clr_wait(void)
{
    put_138(6);
    put_138(0);
}

static inline int get_di(void)
{
    uint8_t c;
    put_138(4);
    WAIT_NOP(6);
    c  = gpio_get_all() & GPIO_DI_MASK;
    put_138(0);
    return c;
}

inline void start_clk(uint32_t counter)
{
    clock_gpio_init(GPOUT0_PIN, CLOCKS_CLK_GPOUT0_CTRL_AUXSRC_VALUE_CLK_SYS, counter);
}

static inline void TOGGLE(void)
{
    gpio_xor_mask(GPIO_TEST_MASK);
}

void wait_clk(int count)
{
    uint32_t prev, cur;
    prev = cur = (gpio_get_all() & GPIO_CLK_MASK);
    while (count > 0) {
        while ((cur = (gpio_get_all() & GPIO_CLK_MASK)) == prev)
            ;
        if (cur == 0) {
            asm volatile ("nop");
            count--;
        }
        prev = cur;
    }
}

