static inline void drain_uart(uart_inst_t *uart)
{
    while (uart_is_readable(uart))
        uart_getc(uart);
}

void init()
{
    uint32_t init_flag = GPIO_DI_MASK|GPIO_DO_MASK|GPIO_138_MASK|GPIO_CLK_MASK|GPIO_TEST_MASK|GPIO_WAIT_MASK;
    // Set up our UART with the required speed.
    stdio_init_all();
    uart_set_baudrate(UART_ID, 57600);
    // Inital UART
    gpio_set_function(UART_TX_PIN, GPIO_FUNC_UART);
    gpio_set_function(UART_RX_PIN, GPIO_FUNC_UART);
    // drain UART RX
    printf("init %08X\n", init_flag);
    // Initial Control-Out
//    drain_uart(UART_ID);
    gpio_init_mask(init_flag);
//    gpio_put_masked(GPIO_138_MASK|GPIO_TEST_MASK|GPIO_CLK_MASK, GPIO_CLK_MASK);
//    gpio_put_masked(GPIO_TEST_MASK, 0);
//    gpio_put_masked(GPIO_CLK_MASK, GPIO_CLK_MASK);
    gpio_set_dir_out_masked(GPIO_DO_MASK|GPIO_138_MASK|GPIO_TEST_MASK|GPIO_CLK_MASK);
    TOGGLE();
    TOGGLE();
//    gpio_set_dir_in_masked(GPIO_DI_MASK);
    // Control-Out .. activate RESET
    init_co(INT_Pin|NMI_Pin|BUSRQ_Pin);
    set_co(INT_Pin|NMI_Pin|BUSRQ_Pin);
    clr_co(RESET_Pin);
    // disable pull-up/down
    for (int i = 0; i <= 22; ++i) {
        if (i != 21)
            gpio_disable_pulls(i);    // cleara pull-up/down
    }
    gpio_disable_pulls(26);
    for (int i = 0; i < 8; ++i) {
        gpio_set_input_hysteresis_enabled(i, 1);
    }
    // init 138
    put_138(0);
    // reset D-FF
    clr_wait();
    put_do(0);


    // init clock, now CLK_SYS is 125MHz, if dividor is 32, then about 4MHz
    start_clk(33);
    wait_clk(6);
    //
    do_test();
    // bootloader
    bootloader();
    // start CPU
    drain_uart(uart0);
}

