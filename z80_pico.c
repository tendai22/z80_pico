static inline uint32_t get_addr(void)
{
    uint32_t ah, al, addr;

    put_138(1);
    WAIT_NOP(6);
    ah = gpio_get_all() & GPIO_DI_MASK;
    put_138(2);
    WAIT_NOP(6);
    al = gpio_get_all() & GPIO_DI_MASK;
    addr = ((ah<<8)|al);
    put_138(0);
    return addr;
}

static inline uint32_t get_al(void)
{
    uint32_t al;
    put_138(2);
    WAIT_NOP(6);
    al = gpio_get_all() & GPIO_DI_MASK;
    put_138(0);
    return al;

}

static inline void refresh_INT_Pin(void)
{
    if (uart_is_readable(UART_ID)) {
        clr_co(INT_Pin);    // INT == H
    } else {
        set_co(INT_Pin);
    }

}

static inline uint32_t uart_fr_to_i8251_SR(uart_inst_t *uart)
{
    uint32_t uart_fr = uart_get_hw(uart)->fr;
    uint32_t data = 0;
    if ((uart_fr & UART_UARTFR_TXFF_BITS) == 0) {
        data |= TXRDY_Bit;
    }
    if ((uart_fr & UART_UARTFR_RXFE_BITS) == 0) {
        data |= RXRDY_Bit;
    }
    if((uart_fr & UART_UARTFR_BUSY_BITS) == 0) {
        data |= TXEMPTY_Bit;
    }
    return data;
}

int main() {
    uint32_t cur;
    int count = 0;
    uint32_t addr, data;
    uint32_t cur_uart, prev_uart = 0;

    init();
    // run CPU
    set_co(RESET_Pin);
    while (1) {
#ifdef NOT_WAIT
        put_138(3);
        WAIT_NOP(4);
        while((cur = (gpio_get_all() & (MREQ_Pin|IORQ_Pin|RFSH_Pin))) != (IORQ_Pin|RFSH_Pin) && cur != (MREQ_Pin|RFSH_Pin))
            ;
#else
        put_138(3);
        WAIT_NOP(6);
        while ((cur = gpio_get_all()) & GPIO_WAIT_MASK) {
            if ((cur_uart = uart_is_readable(UART_ID)) != prev_uart) {
                if (cur_uart) {
                    clr_co(INT_Pin);
                    //count = 60;
                } else {
                    set_co(INT_Pin);
                }
                put_138(3);
                WAIT_NOP(6);
                prev_uart = cur_uart;
            }
        }
#endif
        TOGGLE();
        if ((cur & MREQ_Pin) == 0) {
            // MREQ cycle
            // address
            addr = get_addr();
            if ((cur & RD_Pin) == 0) {
                // Read cycle
                // xfer ram data to Databus
                put_do((data = sram[addr]));
                //if (count > 0) { printf("R: %04X %02X\n", addr, data); } count--;
            } else {
                // Write cycle
                // wait for WR becoming LOW
                put_138(3);
                WAIT_NOP(6);
                while (gpio_get_all() & WR_Pin)
                    ;
                // Now we can get data from DI
                sram[addr] = data = get_di();
                //if (count > 0) { printf("W: %04X %02X\n", addr, data); } count--;
            }  
        } else if ((cur & IORQ_Pin) == 0) {
#if 1
            if ((cur & M1_Pin) == 0) {
                // INTA access
                //printf("INTA\n");
                put_do(0xff);   // interrupt vector 0xff (RST 7)
            } else if ((cur & RD_Pin) == 0) {
                // IORQ cycle
                addr = get_al();
                //if (count > 0) printf("IOADDR: %2X\n", addr);
                if (addr == 0) {
                    // i8251 Data Register Read
                    data = uart_get_hw(UART_ID)->dr;
                    // clear FR->TXFE_BIT
                    refresh_INT_Pin();
                    if (count > 0) { printf("IR: %04X %02X\n", addr, data); } count--;
                    put_do(data);
                } else if (addr == 1) {
                    // i8251 Status Register
                    data = uart_fr_to_i8251_SR(UART_ID);
                    put_do(data);
                }
            } else if ((cur & WR_Pin) == 0) {
                // i8251 Data Register Write
                addr = get_al();
                data = get_di();
                //printf("[%2X]", data);
                if (addr == 0) {
                    //printf("[%02X]", data);
                    uart_get_hw(UART_ID)->dr = data;
                } else if (addr == 1) {
                    //printf("i8251 CR: %02X\n", data);
                } else {
                    printf("IOWR %02X %02X\n", addr, data);
                }
            }
#endif
        }
        clr_wait();
        TOGGLE();
    } // end of while(1)
}
